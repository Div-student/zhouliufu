$(function () {
    $(".loginxz span").click(function () {
        if ($(this).hasClass('xzgl') == false) {
            $(".loginxz span").removeClass('xzgl')
            $(this).addClass('xzgl')
        if ($('.loginym > div[class="ymnone"]')) {
            $(".loginym > div:first-child").addClass('ymnone')
            $(".loginym > div:last-child").removeClass('ymnone')
        } else {
            $(".loginym > div:last-child").addClass('ymnone')
            $(".loginym > div:first-child").removeClass('ymnone')
        }
        }
    })
})