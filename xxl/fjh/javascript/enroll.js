$(function () {
    $(".enrollxz span").click(function () {
        if ($(this).hasClass('xzgl') == false) {
            $(".enrollxz span").removeClass('xzgl')
            $(this).addClass('xzgl')
        if ($('.enrollym > div[class="ymnone"]')) {
            $(".enrollym > div:first-child").addClass('ymnone')
            $(".enrollym > div:last-child").removeClass('ymnone')
        } else {
            $(".enrollym > div:last-child").addClass('ymnone')
            $(".enrollym > div:first-child").removeClass('ymnone')
        }
        }
    })
})